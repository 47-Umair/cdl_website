      <span class="maintext">What To Do While You Wait</span>
      <p class="financing">When you’re excited about starting a career but limited in your potential due to age restrictions, it’s tough. Three years seems like a lifetime away! But with great truck drivers comes great responsibility. Here’s what you can do in the meantime.</p>

      <span class="maintext">Stay Healthy</span>
      <p class="financing">Driving long trips requires good health. Make sure you’re in good shape to meet the demands of the trucker lifestyle! (Physical examinations are mandatory for most CDL seekers, by the way.) Eat well and train hard!</p>

      <span class="maintext">Don’t Do Drugs</span>
      <p class="financing">This is obvious! Getting a CDL requires the successful completion of a five-stage drug test, and random drug tests are possible for all drivers with a CDL. (Many carriers use hair follicle tests that check for drug use in the previous 90 days.) Test positive and you’ll lose your CDL. It’s that simple.
        <br><br>
      Considering that in the United States it’s currently illegal to use illicit substances and alcohol under the age of 21, this is a huge no-no from a commercial truck driving perspective.</p>

      <span class="maintext">Keep a Clean Record</span>
      <p class="financing">One of the biggest things that you need to avoid when starting your career as a truck driver, especially when under the age of 21, is keeping a clean driving record. At-fault accidents, DUI charges, and other dangerous driving scenarios can negatively affect both your potential job outlook and your ability to even get hired by companies. So stay out of trouble!</p>
      
      
    </div><!-- col 5 end -->


    <div class="col-sm-12 col-md-12 col-xl-5 col-lg-5 justify-content-center">
     <span class="maintext">Avoid Speeding Tickets</span>
     <p class="financing">Other things to watch out for are speeding tickets. Most drivers have disobeyed the posted speed limits within their state at least once. As a hopeful, soon-to-be truck driver, you will want to avoid this at all costs! Not only can speeding tickets disqualify you from the license and potential job opportunities, but the danger also posed to both yourself and other drivers on the road is elevated when exceeding state-mandated speed guidance. (But you know that already!) Car accidents can be life-altering — it’s potentially even worse for drivers operating heavy vehicles like trucking rigs and large delivery trucks.</p>

     <span class="maintext">Stay Local</span>
     <p class="financing">
     Get a local driving job and a Class B CDL license to gain experience. A Class B CDL is a truck with a GVWR (gross vehicle weight rating) of 26,001 pounds including a towed capacity not exceeding 10,000 pounds. A popular way to make the most out of a Class B CDL is with industry-purposed vehicles like some larger firetrucks, dump trucks, and bucket trucks.</p>

     <span class="maintext">Still Want to Pursue Your Class A CDL at 18?</span>
     <p class="financing">
      Driving for local logistics companies or regional companies within your state’s border is probably the best option as it’ll give you more real-world experience driving a truck and trailer with a GVWR  of 26,001 pounds and a trailer weighing over 10,000 pounds.
      <br><br>
    Additionally, if you’re in the military, the Military Skills Test Waiver Program, available in every state, lets service personnel with a clean driving record of at least two years waive the need for a CDL driving test (skills test). Keep in mind that you must have held a military job within the last 12 months when applying for the waiver.</p>

