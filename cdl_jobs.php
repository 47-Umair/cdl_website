      <span class="maintext">One-Way Truckloads</span>
      <p class="financing">Drivers who work one-way truckload jobs transport freight from one location to another. There’s a variety of customers to serve through one-way truckloads nationwide and even into Canada and Mexico. If you’re not looking for long-haul jobs, some truckloads are delivered via a relay program that has drivers home every night.</p>

      <span class="maintext">Owner Operator</span>
      <p class="financing">Owner operator truck drivers own their own trucking business. They can either own or lease their truck, but are responsible for finding freight to haul and the daily activities of owning a trucking company. There’s a lot of freedom that comes with owning your own trucking business, but a greater amount of responsibility to ensure your success.</p>

      <span class="maintext">Intermodal Jobs</span>
      <p class="financing">Intermodal truckers are responsible for hauling freight between railyards and customers. Because the train covers the longest leg of the journey, intermodal jobs are for solo drivers. This means you could spend more time at home.</p>

      <span class="maintext">Flatbed Jobs</span>
      <p class="financing">Large or unusually shaped freight are hauled using commercial flatbed trailers. The driver is responsible for using the proper straps, tarps, and other equipment to ensure the cargo is safely secured. Because these jobs are more demanding, they’re often paid higher wages than the average trucking job.</p>
      
      
