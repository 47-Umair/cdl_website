      <p class="financing"><span class="fw-bold">The Physical Exam covers:<br>General Appearance</span><br><br>
        This simple check ensures you look healthy and alert.<br> </p>
        <span class="fw-bold">Spine</span>
        <p class="financing">The spine and other musculoskeletal areas get checked for debilitating issues. These include tender areas, poor range of motion or limited motion, prior surgeries, and anything else relevant.</p>

        <span class="fw-bold">Ear</span>
        <p class="financing">This differs from what you usually get at a hearing test. Instead, the doctor looks for specific issues such as a perforated eardrum or tympanic membrane. </p>

        <span class="fw-bold">Eye</span>
        <p class="financing">This checkup is more like the extras an eye doctor checks for during an eye exam. It’s not a vision test. Instead, this includes looking for macular degeneration, glaucoma, cataracts, and more.</p>

        <span class="fw-bold">Neurological</span>
        <p class="financing">The neurological part of the exam covers a wide range of conditions. These include asymmetric deep tendon reflexes, impaired equilibrium, and speech pattern or coordination ataxia.</p>
        
        <span class="fw-bold">Throat and Mouth</span>
        <p class="financing">This part of the exam looks for issues with the throat and mouth. This includes impairment with swallowing or breathing.</p>
      </div>

      <div class="col-sm-12 col-md-12 col-xl-5 col-lg-5">
        <span class="fw-bold">Extremities</span>
        <p class="financing">This exam looks for impaired extremities or limbs.</p>

        <span class="fw-bold">Viscera and Abdomen</span>
        <p class="financing">The examiner looks for problems with muscle weakness, viscera (the internal organs), and things like an enlarged liver.</p>
        
        <span class="fw-bold">Heart</span>
        <p class="financing">Issues looked for in the heart include a pacemaker, extra or abnormal sounds, a heart murmur, etc.</p>

        <span class="fw-bold">Genito-urinary</span>
        <p class="financing">TThis is a search for a hernia or hernias.</p>

        <span class="fw-bold">Vascular</span>
        <p class="financing">This is the same thing as your circulatory system and the doctor will look for things like varicose veins, carotid, or an abnormal pulse – for example.</p>

        <span class="fw-bold">Chest and Lungs</span>
        <p class="financing">The doctor ensures there are no issues with areas like your respiratory function. He also looks for abnormal breathing and cyanosis, which is low oxygen or poor circulation</p>
