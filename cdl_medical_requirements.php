      <p class="financing">The <a href="#" class="text-primary">FMCSA’s regulations<sup>2</sup></a> state that a truck driver is physically qualified to drive a commercial motor vehicle (the truck of CDL drivers) if the driver:</p>

      
      <ul class="text-center lists">
        <li><p>Has met medical examination requirement</p></li>
        <li><p>Has no loss of a foot, a leg, a hand, or an arm, or has been granted a skill performance evaluation certificate</p></li>

        <li><p>Doesn’t have an impairment of hand or finger that interferes with the ability to hold the wheel and drive the truck</p></li>

        <li><p>Doesn’t have an impairment of arm, foot, or leg which interferes with the ability to perform normal tasks associated with operating a commercial motor vehicle</p></li>

        <li><p>Has no established medical history or clinical diagnosis of diabetes requiring insulin for control</p></li>
        <li><p>Doesn’t have any breathing problems (or respiratory dysfunction) that could be a problem while driving</p></li>

        <li><p>Has no current clinical diagnosis of high blood pressure that could interfere with trucking responsibilities</p></li>
        
        <li><p>Has no established medical history or clinical diagnosis of rheumatic, arthritic, orthopedic, muscular, neuromuscular, or vascular disease which interferes with his/her ability to control and operate a commercial motor vehicle safely</p></li>
        
        <li><p>Doesn’t have a diagnosis or medical history of epilepsy or any other condition that could cause a driver to lose consciousness while driving</p></li>
        
        <li><p>Has no current clinical diagnosis of alcoholism</p></li>
        
        <li><p>Has no current clinical diagnosis of myocardial infarction, angina pectoris, coronary insufficiency, thrombosis, or any other cardiovascular disease of a variety known to be accompanied by syncope, dyspnea, collapse, or congestive cardiac failure (This one looks complicated, but it’s basically excluding drivers with heart conditions that could overtake them while driving or performing trucking duties. Check with your doctor or physician for more information.)</p></li>
        
        <li><p>Has no mental, nervous, organic, or functional disease or psychiatric disorder likely to interfere with his/her ability to drive a commercial motor vehicle safely</p></li>
        
        <li><p>Can see well enough at a distance without corrective lenses and can tell the difference in light colors</p></li>
        
        <li><p>Meets hearing standards and can pass a hearing test</p></li>
        
        <li><p>Does not use narcotics, amphetamines, or any other illegal drugs. Prescribed drugs can’t interfere with driving, either</p></li>
        
      </ul>

      <p class="financing">All of this medical information might seem like a headache, but these qualifications make for a safer highway for all drivers. The best way to ensure you currently meet all CDL truck driver medical requirements is to speak with a representative from your truck driving school.</p>
      

      
      
