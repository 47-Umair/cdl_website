      <span class="maintext">What Does a DOT Physical Consist of?</span>
      <p class="financing"><i>In this article, we’ll go over what the Department of Transportation or (DOT) physical covers, what to bring, and where to learn more about the process.</i></p>
      
      <span class="maintext">Why Are Physicals Required for Truck Drivers?</span>
      <p class="financing">The Federal Motor Carrier Safety Administration (FMCSA) is the lead government agency responsible for regulating and providing safety oversight of Commercial Motor Vehicles (CMV) and CMV drivers. The FMCSA’s mission is to reduce crashes, injuries, and fatalities involving large trucks and busses. One way the FMCSA upholds this mission is by requiring commercial drivers possessing a Commercial Driver’s License to obtain and maintain a valid Medical Examiner’s Certification, more commonly known as a DOT physical. The DOT physical completed by a Certified Medical Examiner ensures that the CDL holder meets DOT physical qualifications for a driver. If you are unable to obtain a valid Medical Examiner’s Certificate, trucking carriers cannot allow you to perform safety-sensitive functions which include driving for them.</p>
      <span class="maintext">What To Bring to the DOT Physical</span>
      <p class="financing">There are specific requirements for the DOT physical that involve the health of the truck driver. You should fill out the health history questionnaire truthfully and accurately. Doing this prior to your appointment saves time. Bring a list of all medications you take, if applicable. This comprises the names of the medication, dosages, dosage regimen, and the names and addresses of your physicians.<br><br>

      There are certain situations where more items or documents may need to come with you to the examination. Examples include:</p>

      <ul class="text-center lists">
        <li><p>Drivers with heart conditions. Bring as much documentation as you have. At a minimum, they require a cardiologist’s letter. This should include a “safe to work” recommendation, health history, and a list of medications.</p></li>

        <li><p>Drivers with diabetes. Bring the most recent lab results showing blood sugar logs and Hemoglobin A1C (HgAIC).</p></li>
        
        <li><p>Drivers with hearing or vision problems. When applicable, bring hearing aids, contact lenses, or eyeglasses.</p></li>

      </ul>
      <p class="financing">These are the most common, yet there is a variety of circumstances where other items or documents may be required. These are:</p>

      <ul class="text-center lists">
        <li><p>Drivers with high blood pressure</p></li>
        
        <li><p>Drivers who have nighttime sleep disturbance (sleep apnea) and use a CPAP machine including a 30-day CPAP compliance report</p></li>
        <li><p>Drivers who take blood thinners like Coumadin</p></li>
      </ul>

      <span class="maintext">The Physical</span>
      <p class="financing">The physical itself includes specific components. These include vision, hearing, blood pressure, blood rate, urinalysis, and a physical exam.
        <br><br>
        <span class="fw-bold">Vision Test</span>
        <br>
        Whether the vision has correction, all drivers must have 20/40 vision at a minimum. The other requirement is 70″ peripheral in the horizontal meridian, at a minimum. This measurement is in both the right and left eyes.
        <br><br>
        <span class="fw-bold">Hearing Test</span>
        <br>
        The <a href="#" class="text-primary">FMCSA</a> requires:<br><br>
        <i>The tests for hearing are the forced whisper test or an audiometric test. For the whispered voice test, the driver should be 5 feet from the examiner with the ear being tested turned toward the examiner. The other ear is covered. Using the breath which remains after a forced expiration, the examiner whispers words or random numbers such as 66,18.23. The examiner should not use only sibilants (s-sounding test materials). If the individual fails the whisper test, the examiner should administer the audiometric test.</i>
        <br><br>
        <span class="fw-bold">Pulse Rate and Blood Pressure</span><br>
        This test is self-explanatory. The examiner uses it to detect an irregular heartbeat or high blood pressure.
        <br><br>
        <span class="fw-bold">Urinalysis</span><br>
        The urine test provides results for medical issues of the kidneys or things like diabetes. It is not a drug screening.
      </p>

