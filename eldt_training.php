        <span class="maintext">What is ELDT?</span>
        <p class="financing">The <a href="#" class="text-primary">ELDT</a> is a set of minimum standards for <a href="#" class="text-primary">commercial driver</a> education that all training providers must follow. WIth this mandate is in place, training providers will have to certify to the FMCSA that their training programs meet minimum standards in order to continue training commercial drivers.<br><br>

          ELDT training is comprised of classroom (theory) education and skills training. Students will learn approximately 30 subjects within the classroom portion of the training and must score a minimum of <a href="#" class="text-primary">80 percent</a> on a theory assessment to meet minimum program requirements. To qualify for the CDL exam, students will receive specified behind-the-wheel training, as well as have a trainer’s endorsement stating that they are proficient in the CDL skills curriculum.<br><br>

