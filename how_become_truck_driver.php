        <span class="maintext">Medical</span>
        <p class="financing">Some medical conditions may prevent you from becoming a truck driver due to various Federal and state regulations relative to safety. The physical examination you take for your medical certificate confirms that you are healthy enough to safely perform the demanding job of a Class A Truck Driver and keep our nation’s roads safe.
          <br><br>
          These conditions are mandated by the federal government and are checked for at the required Department of Transportation (DOT) physical. A DOT physical examination must be conducted by a licensed medical examiner listed on the Federal Motor Carrier Safety Administration (FMCSA) National Registry.<br><br>

        To start, you must fill out your health history. This includes information on any surgeries you’ve had, medications you are currently using, specific diagnoses, substance issues and symptoms. From there, the medical examiner tests various parts of your health. They test your pulse, blood pressure, vision in both eyes and hearing. You must also pass a urinalysis test. This is used for drug testing and detection of blood, sugar, or protein in the urine. During the physical examination, your doctor checks your overall health and each body system, marking it as normal or abnormal. The doctor then sends the DOT physical form directly to the licensing agency in your state.</p>

        <span class="maintext">Criminal Background</span>
        <p class="financing">While having a felony on your record doesn’t affect your ability to get a Class A CDL license, it may affect your ability to get employed at a trucking carrier. Not all felons will qualify for jobs after obtaining their CDL license since several variables like the type of conviction, time since completion of sentence and whether the felony was violent or not will affect employment.</p>

        <span class="maintext">Driving Record</span>
        <p class="financing">CDL training schools and trucking carriers look for drivers who have proven to be skilled and safe on the road. Speeding tickets, DUIs, and other traffic violations are important factors when getting a CDL but don’t automatically mean you’re disqualified.</p>

        <span class="maintext">Review the Entry Requirements for Training Schools</span>
        <p class="financing">Before you receive a CDL, you’ll need a regular driver’s license. Be sure your license is valid and current. It would be best if you also had a high school diploma or GED for most training programs.<br><br>

        Compare community college and training school programs to determine the best option for your situation. At your local IEC DBA Drivers School location, you can enjoy affordable and hands-on training to jumpstart your career. Take the entrance exam at your local location or a third-party location before starting your truck driver training.</p>

        <span class="maintext">Study for Your CDL Exam</span>
