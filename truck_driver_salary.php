      <span class="maintext">Types of Truck Driving Jobs</span>
      

      <ul class="text-center lists mt-3">
        <li><p><span class="fw-bold">Solo OTR Truck Driver–</span> Solo truck drivers make up a large percentage of the workforce. Their earnings vary depending on the available bonuses offered by their carrier. That’s why having the option to choose your carrier is such a great benefit! These drivers average up to $61,152* or more their first year!</p></li>

        <li><p><span class="fw-bold">Team OTR Truck Driver–</span> Driving with a fellow truck driver means you can log more miles together. With two drivers, the truck never stops moving. While you take your rest period, your partner is driving. Because the drivers split the total combined mileage, there is typically more money to be made.</p></li>

        <li><p><span class="fw-bold">Dedicated Truck Driver–</span> A truck driver who makes runs and deliveries for a single company (such as Walmart or Home Depot) is called a dedicated driver. They have a specified run or route for their dedicated company where they transport only the company’s materials. These drivers typically get a bit more home time than standard cross-country truck drivers. The trade-off is that these positions typically require you to handle the freight you are delivering. These positions can sometimes pay less than truck drivers who log more miles driving cross country. Dedicated drivers typically average $61,152* .</p></li>
        
      </ul>
      

      <span class="maintext">Bonuses</span>
      <p class="financing">In addition to a regular paycheck, you may also be able to make money through various bonuses as a Class A CDL trained truck driver. Each trucking carrier is different, but many offer bonuses like:</p>

      <ul class="text-center lists">
        <li><p><span class="fw-bold">Monthly mileage –</span> Some carriers pay their drivers extra when they achieve company thresholds for miles. Earn money for driving the longer distances and earn extra pay just because you drove them with your company!</p></li>  
        
        <li><p><span class="fw-bold">Sign-on bonus –</span> To attract new truck drivers, competing carriers will sometimes offer sign-on bonuses for drivers who choose their company. The amount differs from carrier-to-carrier and can be paid as a whole sum or through gradual payments.</p></li>
        
        <li><p><span class="fw-bold">Fuel efficiency –</span> By using various fuel-saving practices, you prove to your carrier that you are a cost-conscience truck driver. Carriers appreciate that, and many reward their drivers for cutting down on fuel.</p></li>

        <li><p><span class="fw-bold">Safety pay – </span> Carriers strive for safe driving and may even offer bonuses for proven safety out on the highways. Proving that you can safely drive a tractor-trailer means more than good business, it means saving lives!</p></li>

        <li><p><span class="fw-bold">Layover pay –</span> If you are inconvenienced or stalled in the delivery process, some carriers offer layover pay as a way of compensating truck drivers for the time lost.</p></li>

        <li><p><span class="fw-bold">Clean DOT Inspections –</span> Clean Department of Transportation inspections are good for both the driver and the company. Carriers know this, and many pay their truck drivers for clean inspections.</p></li>

        <li><p><span class="fw-bold">Referrals –</span> Having a friend sign up for your carrier company could lead to hefty bonuses. CDL-trained truck drivers are in high demand, and carriers are ready to pay for them!</p></li>
